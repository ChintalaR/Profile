function contact(){
    document.getElementById('contact').style.cssText = 'display:block';
    document.getElementById('social').style.cssText = 'display:none';
    document.getElementById('coding').style.cssText = 'display:none';
}
function social(){
    document.getElementById('contact').style.cssText = 'display:none';
    document.getElementById('social').style.cssText = 'display:block';
    document.getElementById('coding').style.cssText = 'display:none';
}
function coding(){
    document.getElementById('contact').style.cssText = 'display:none';
    document.getElementById('social').style.cssText = 'display:none';
    document.getElementById('coding').style.cssText = 'display:block';
}